scene.onHitWall(SpriteKind.Player, function (sprite, location) {
    jump_counter = 0
    if (Hero.vy > 300) {
        scene.cameraShake(2, 200)
        for (let index = 0; index < 50; index++) {
            boom = sprites.create(explode._pickRandom(), SpriteKind.explode)
            boom.setPosition(Hero.x - randint(-10, 10), Hero.y + 10)
            boom.setVelocity(randint(-40, 40), randint(-10, -70))
            boom.ay = 50
            boom.lifespan = 8000
        }
        if (Hero.vx > 80) {
            Hero.setVelocity(60, -60)
        } else {
            if (Hero.vx > 50) {
                Hero.setVelocity(50, 0)
            }
        }
    }
    if (Hero.vx < -80) {
        Hero.setVelocity(-60, -60)
    } else {
        if (Hero.vx < -50) {
            Hero.setVelocity(-50, 0)
        }
    }
})
controller.up.onEvent(ControllerButtonEvent.Pressed, function () {
    if (jump_counter == 0) {
        Hero.vy = -150
        jump_counter += 1
    }
})
controller.A.onEvent(ControllerButtonEvent.Pressed, function () {
    if (controller.right.isPressed()) {
        if (dashcooldown.value == dashcooldown.max) {
            Hero.setVelocity(200, -50)
            dashcooldown.value = 0
        }
    }
    if (controller.left.isPressed()) {
        if (dashcooldown.value == dashcooldown.max) {
            Hero.setVelocity(-200, -50)
            dashcooldown.value = 0
        }
    }
})
